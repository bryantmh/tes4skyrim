# The engine-side graph interface: SSE's movement controller / combat / AI
# bind these by name at graph init (every vanilla creature behavior declares
# them — vanilla dogbehavior has 65 variables; this is the engine-set/read
# subset).  A graph with NO variables leaves the movement hookup dead: the
# actor plays its start state forever and never walks or reacts.
# Third field = initial word value.  Vanilla dogbehavior inits IsAttackReady
# and bEquipOK to 1 — the combat controller reads them before ever sending
# an attackStart_* event, so a 0 default means the actor follows its target
# but NEVER attacks (2026-07-09 no-attacks root cause, with the weaponDraw
# reply below).  iCombatStance is engine-written on stance changes and
# drives the StartCombat/StopCombat expression pair.
ENGINE_VARIABLES = [
    ('Speed', 'REAL', 0), ('Direction', 'REAL', 0), ('TurnDelta', 'REAL', 0),
    ('TurnDeltaDamped', 'REAL', 0), ('SpeedSampled', 'REAL', 0),
    ('staggerMagnitude', 'REAL', 0), ('staggerDirection', 'REAL', 0),
    ('iState', 'INT32', 0), ('iGetUpType', 'INT32', 0),
    ('iCharacterSelector', 'INT32', 0), ('iCombatStance', 'INT32', 0),
    ('IsAttacking', 'BOOL', 0), ('IsAttackReady', 'BOOL', 1),
    ('bEquipOK', 'BOOL', 1),
    ('IsRecoiling', 'BOOL', 0), ('IsStaggering', 'BOOL', 0),
    ('IsBleedingOut', 'BOOL', 0), ('IsBashing', 'BOOL', 0),
    ('bAnimationDriven', 'BOOL', 0), ('bAllowRotation', 'BOOL', 0),
    ('bHeadTracking', 'BOOL', 0), ('bCanHeadTrack', 'BOOL', 0),
    ('bDisableHeadTrack', 'BOOL', 0), ('bForceIdleStop', 'BOOL', 0),
]


def movement_type_names(name: str) -> list:
    """The creature's engine movement-type names — the CONTRACT that makes an
    actor movable at all (2026-07-09, the tc-can't-move root cause).

    The engine registers an actor's movement types by enumerating the ROOT
    behavior graph's variables named `iState_<X>` and looking up the MOVT
    record whose MNAM == <X> (ck-cmd RetargetCreature.cpp does exactly this
    rename+MOVT-clone when retargeting creatures; vanilla dogbehavior
    declares iState_DogDefault/iState_DogRun matching MOVT MNAM DogDefault/
    DogRun). A graph with NO iState_* variables gives the movement
    controller ZERO movement types: the actor cannot move even under
    console `tc` control, no locomotion events are ever sent, and it idles
    forever — records, packages, clips and cache all being correct.

    These names are declared as INT32 graph variables here AND emitted as
    MOVT records by tes5_import/creature_races.py (via the manifest), so
    consistency is by construction, like the ATKE attack events.
    """
    base = f'TES4{name.lower()}'
    return [f'{base}Default', f'{base}Run']


def build_behavior_xml(behavior_name: str, clips: dict,
                       hit_times: dict = None,
                       movement_types: list = None,
                       speeds: dict = None,
                       ragdoll: dict = None) -> str:
    """The generated v1 state machine (see module docstring).

    hit_times: {state_name: [seconds]} — Oblivion 'Hit' text-key times for
    attack clips, emitted as preHitFrame/HitFrame triggers (the engine's
    attack-damage contract; without HitFrame an attack never deals damage).

    movement_types: MOVT MNAM strings (movement_type_names()) declared as
    `iState_<X>` INT32 variables — the engine's movement-type registration
    contract; without them the actor cannot move at all.

    speeds: {'walk': u/s, 'run': u/s, ...} clip root-motion speeds — anchor
    weights for the MoveForward parametric walk/run blend (vanilla
    ForwardWalkBlend: children weighted by natural clip speed, blend
    parameter bound to SpeedSampled, flags=17 SYNC|PARAMETRIC).

    ragdoll: hkx_ragdoll.ragdoll_info() dict — enables the death/ragdoll
    wrapper states (DeathAnimation/Ragdoll/RagdollInstant routing, mirrors
    the vanilla dogbehavior root SM). Oblivion creatures have no death
    animations: death IS the ragdoll.
    """
    hit_times = hit_times or {}
    movement_types = movement_types or []
    speeds = speeds or {}
    # ---- event vocabulary: standard + per-attack ----
    events = ['moveStart', 'moveStop', 'moveForward', 'moveBackward',
              'turnLeft', 'turnRight', 'turnStop', 'swimStart', 'swimStop',
              'recoilStart', 'recoilStop', 'recoilLargeStart',
              'staggerStart', 'staggerStop',
              'combatStanceStart', 'combatStanceStop',
              'weaponDraw', 'weaponSheathe', 'weaponSwing',
              'attackStop', 'returnToDefault', 'deathStart', 'IdleStop',
              'DeathAnimation', 'Ragdoll', 'RagdollInstant',
              'AddRagdollToWorld', 'RemoveCharacterControllerFromWorld',
              'SoundPlay', 'preHitFrame', 'HitFrame', 'FootFront', 'FootBack']
    attack_events = build_attack_events(clips)
    events += attack_events
    eid = {n: i for i, n in enumerate(events)}

    if not clips['idle']:
        raise ValueError('creature has no idle clip')

    pf = HkxPackfile(first_id=80)

    # blend effect: smooth 0.3s (vanilla locomotion default)
    fx_smooth = pf.add('hkbBlendingTransitionEffect')
    for obj, nm, dur, mode in ((fx_smooth, 'BlendSmooth', 0.3,
                                'SELF_TRANSITION_MODE_CONTINUE_IF_CYCLIC'),):
        obj.param('variableBindingSet', 'null')
        obj.param('userData', 0)
        obj.param('name', nm)
        obj.param('selfTransitionMode', mode)
        obj.param('eventMode', 'EVENT_MODE_DEFAULT')
        obj.param('duration', f'{dur:.6f}')
        obj.param('toGeneratorStartTimeFraction', '0.000000')
        obj.param('flags', 'FLAG_IGNORE_FROM_WORLD_FROM_MODEL')
        obj.param('endMode', 'END_MODE_NONE')
        obj.param('blendCurve', 'BLEND_CURVE_SMOOTH')

    # variable table is fixed up-front so bindings can reference indices.
    # iState family: vanilla-style movement-type tags (dogbehavior:
    # iState_DogDefault=30/iState_DogRun=31, iState initialized to Default).
    variables = ([(n, t, iv) for n, t, iv in ENGINE_VARIABLES]
                 + [(f'iState_{mt}', 'INT32', 30 + i)
                    for i, mt in enumerate(movement_types)])
    var_values = [30 if n == 'iState' else iv for n, _t, iv in variables]
    vidx = {n: i for i, (n, _t, _iv) in enumerate(variables)}

    def _binding_set(pairs):
        """hkbVariableBindingSet from (memberPath, variable name) pairs."""
        b = pf.add('hkbVariableBindingSet')
        b.param_raw('bindings',
                    '\n'.join(_BINDING_TMPL.format(member=m,
                                                   var_index=vidx[v])
                              for m, v in pairs),
                    numelements=len(pairs))
        b.param('indexOfBindingToEnable', -1)
        return b

    def _clip(name, kf_path, looping, triggers_ref='null', rate=1.0):
        clip = pf.add('hkbClipGenerator')
        clip.param('variableBindingSet', 'null')
        clip.param('userData', 0)
        clip.param('name', name)
        clip.param('animationName',
                   'Animations\\' + _clip_state_name(kf_path) + '.hkx')
        clip.param('triggers', triggers_ref)
        clip.param('cropStartAmountLocalTime', '0.000000')
        clip.param('cropEndAmountLocalTime', '0.000000')
        clip.param('startTime', '0.000000')
        clip.param('playbackSpeed', f'{rate:.6f}')
        clip.param('enforcedDuration', '0.000000')
        clip.param('userControlledTimeFraction', '0.000000')
        clip.param('animationBindingIndex', -1)
        clip.param('mode', 'MODE_LOOPING' if looping else 'MODE_SINGLE_PLAY')
        clip.param('flags', 0)
        return clip

    def _trans_array(items):
        """hkbStateMachineTransitionInfoArray from
        (event_id, to_state, flags) tuples."""
        arr = pf.add('hkbStateMachineTransitionInfoArray')
        arr.param_raw(
            'transitions',
            '\n'.join(_TRANSITION_TMPL.format(effect=fx_smooth.ref,
                                              event_id=e, to_state=s,
                                              flags=f)
                      for e, s, f in items),
            numelements=len(items))
        return arr

    def _events_array(names):
        arr = pf.add('hkbStateMachineEventPropertyArray')
        arr.param_raw('events', '\n'.join(
            '<hkobject>\n'
            f'\t<hkparam name="id">{eid[n]}</hkparam>\n'
            '\t<hkparam name="payload">null</hkparam>\n'
            '</hkobject>' for n in names), numelements=len(names))
        return arr

    def _state(state_id, name, generator_ref, transitions=None,
               exit_events=None, enter_events=None):
        # referenced objects must be added BEFORE the stateInfo (hkxcmd's
        # parser rejects forward references)
        trans_ref = _trans_array(transitions).ref if transitions else 'null'
        enter_ref = (_events_array(enter_events).ref if enter_events
                     else 'null')
        exit_ref = _events_array(exit_events).ref if exit_events else 'null'
        st = pf.add('hkbStateMachineStateInfo')
        st.param('variableBindingSet', 'null')
        st.param_array('listeners', [])
        st.param('enterNotifyEvents', enter_ref)
        st.param('exitNotifyEvents', exit_ref)
        st.param('transitions', trans_ref)
        st.param('generator', generator_ref)
        st.param('name', name)
        st.param('stateId', state_id)
        st.param('probability', '1.000000')
        st.param('enable', True)
        return st

    def _make_sm(name, states, start_id=0, wildcard_ref='null'):
        m = pf.add('hkbStateMachine')
        m.param('variableBindingSet', 'null')
        m.param('userData', 0)
        m.param('name', name)
        m.param_raw('eventToSendWhenStateOrTransitionChanges', (
            '<hkobject>\n\t<hkparam name="id">-1</hkparam>\n'
            '\t<hkparam name="payload">null</hkparam>\n</hkobject>'))
        m.param('startStateChooser', 'null')
        m.param('startStateId', start_id)
        m.param('returnToPreviousStateEventId', -1)
        m.param('randomTransitionEventId', -1)
        m.param('transitionToNextHigherStateEventId', -1)
        m.param('transitionToNextLowerStateEventId', -1)
        m.param('syncVariableIndex', -1)
        m.param('wrapAroundStateId', False)
        m.param('maxSimultaneousTransitions', 32)
        m.param('startStateMode', 'START_STATE_MODE_DEFAULT')
        m.param('selfTransitionMode', 'SELF_TRANSITION_MODE_NO_TRANSITION')
        m.param_array('states', [s.ref for s in states])
        m.param('wildcardTransitions', wildcard_ref)
        return m

    def _parametric_blend(name, plan):
        """Vanilla-style SYNC|PARAMETRIC speed blend: children are the same
        clips at scaled playbackSpeeds, each anchored (weight) at its true
        animation speed, blendParameter driven by SpeedSampled."""
        children = []
        for cnm, kf, rate, anchor in plan:
            child_clip = _clip(cnm, kf, True, rate=rate)
            ch = pf.add('hkbBlenderGeneratorChild')
            ch.param('variableBindingSet', 'null')
            ch.param('generator', child_clip.ref)
            ch.param('boneWeights', 'null')
            ch.param('weight', f'{anchor:.6f}')
            ch.param('worldFromModelWeight', '1.000000')
            children.append(ch)
        bind = _binding_set([('blendParameter', 'SpeedSampled')])
        blender = pf.add('hkbBlenderGenerator')
        blender.param('variableBindingSet', bind.ref)
        blender.param('userData', 0)
        blender.param('name', name)
        blender.param('referencePoseWeightThreshold', '0.000000')
        blender.param('blendParameter', '1.000000')
        blender.param('minCyclicBlendParameter', '0.000000')
        blender.param('maxCyclicBlendParameter', '1.000000')
        blender.param('indexOfSyncMasterChild', -1)
        blender.param('flags', 17)
        blender.param('subtractLastChild', False)
        blender.param_array('children', [c.ref for c in children])
        return blender

    # =====================================================================
    # Nested state machines (the vanilla quadruped topology, 2026-07-16).
    # The old FLAT graph made every engine event a root wildcard: combat
    # facing-adjustments (turnLeft/turnRight, sent continuously) hijacked
    # the whole graph — visible as constant left/right body-whipping — and
    # aborted every attack mid-swing (the "never attacks" report).  Vanilla
    # nests turns inside the Standing machine (unreachable while moving or
    # attacking) and enters attacks only via LOCAL transitions from
    # DefaultState.
    # =====================================================================
    loco = clips['locomotion']

    # ---- LocomotionBehavior: Forward(0) <-> Backward(1) ----
    loco_sm = None
    if 'MoveForward' in loco:
        fplan = speed_blend_plan(clips, speeds)
        fwd_gen = (_parametric_blend('ForwardSpeedBlend', fplan) if fplan
                   else _clip('MoveForward', loco['MoveForward'], True))
        has_back = 'MoveBackward' in loco
        fwd_state = _state(
            0, 'ForwardLocomotionState', fwd_gen.ref,
            transitions=([(eid['moveBackward'], 1, _F_LOCAL)]
                         if has_back else None))
        loco_states = [fwd_state]
        if has_back:
            bplan = backward_blend_plan(clips, speeds)
            back_gen = (_parametric_blend('BackwardSpeedBlend', bplan)
                        if bplan
                        else _clip('MoveBackward', loco['MoveBackward'],
                                   True))
            loco_states.append(_state(
                1, 'BackwardLocomotionState', back_gen.ref,
                transitions=[(eid['moveForward'], 0, _F_LOCAL)]))
        loco_sm = _make_sm('LocomotionBehavior', loco_states)

    # ---- StandingIdleBehavior: NonCombatIdle(0) <-> CombatIdle(1) ----
    idle_clip = _clip('Idle', clips['idle'], True)
    combat_clip = _clip('CombatStance', clips['idle'], True)
    standing_idle_sm = _make_sm('StandingIdleBehavior', [
        _state(0, 'NonCombatIdleState', idle_clip.ref,
               transitions=[(eid['combatStanceStart'], 1, _F_LOCAL)]),
        _state(1, 'CombatIdleState', combat_clip.ref,
               transitions=[(eid['combatStanceStop'], 0, _F_LOCAL)]),
    ])

    # ---- StandingBehavior: idle switch + looping turn-in-place states ----
    has_tr = 'TurnRight' in loco
    has_tl = 'TurnLeft' in loco
    idle_trans = []
    if has_tr:
        idle_trans.append((eid['turnRight'], 1, _F_LOCAL))
    if has_tl:
        idle_trans.append((eid['turnLeft'], 2, _F_LOCAL))
    standing_states = [_state(0, 'StandingIdleState', standing_idle_sm.ref,
                              transitions=idle_trans or None)]
    if has_tr:
        tr_trans = [(eid['turnStop'], 0, _F_LOCAL)]
        if has_tl:
            tr_trans.insert(0, (eid['turnLeft'], 2, _F_LOCAL))
        standing_states.append(_state(
            1, 'LoopingTurnRight',
            _clip('TurnRight', loco['TurnRight'], True).ref,
            transitions=tr_trans))
    if has_tl:
        tl_trans = [(eid['turnStop'], 0, _F_LOCAL)]
        if has_tr:
            tl_trans.insert(0, (eid['turnRight'], 1, _F_LOCAL))
        standing_states.append(_state(
            2, 'LoopingTurnLeft',
            _clip('TurnLeft', loco['TurnLeft'], True).ref,
            transitions=tl_trans))
    standing_sm = _make_sm('StandingBehavior', standing_states)

    # ---- DefaultBehavior: Standing(0) <-> Locomotion(1) ----
    default_states = [_state(
        0, 'StandingState', standing_sm.ref,
        transitions=([(eid['moveStart'], 1, _F_LOCAL)] if loco_sm else None))]
    if loco_sm:
        default_states.append(_state(
            1, 'LocomotionState', loco_sm.ref,
            transitions=[(eid['moveStop'], 0, _F_LOCAL)]))
    default_sm = _make_sm('DefaultBehavior', default_states)

    # ---- root state machine: DefaultState + single-play interrupts ----
    # Attack states wrap their clip in the vanilla IsActive modifier: while
    # the state is active it drives IsAttacking=1 (the combat controller's
    # in-progress flag), bAllowRotation=1 (target tracking during the
    # swing), bDisableHeadTrack=1.  Without IsAttacking the engine keeps
    # steering mid-swing.
    attack_isactive_bind = _binding_set([
        ('bIsActive0', 'IsAttacking'),
        ('bIsActive1', 'bAllowRotation'),
        ('bIsActive2', 'bDisableHeadTrack')])
    attack_isactive = pf.add('BSIsActiveModifier')
    attack_isactive.param('variableBindingSet', attack_isactive_bind.ref)
    attack_isactive.param('userData', 2)
    attack_isactive.param('name', 'BSIsActiveModifier_IsAttacking')
    attack_isactive.param('enable', True)
    for i in range(5):
        attack_isactive.param(f'bIsActive{i}', False)
        attack_isactive.param(f'bInvertActive{i}', False)
    attack_ml = pf.add('hkbModifierList')
    attack_ml.param('variableBindingSet', 'null')
    attack_ml.param('userData', 1)
    attack_ml.param('name', 'AttackModifierList')
    attack_ml.param('enable', True)
    attack_ml.param_array('modifiers', [attack_isactive.ref])

    def _clip_triggers(st_name, end_evt):
        items = []
        for t in hit_times.get(st_name, []):
            items.append(_TRIGGER_TMPL.format(
                time=max(0.0, t - 0.3), event_id=eid['weaponSwing'],
                rel='false'))
            items.append(_TRIGGER_TMPL.format(
                time=max(0.0, t - 0.1), event_id=eid['preHitFrame'],
                rel='false'))
            items.append(_TRIGGER_TMPL.format(
                time=t, event_id=eid['HitFrame'], rel='false'))
        if end_evt:
            items.append(_TRIGGER_TMPL.format(
                time=0.0, event_id=eid[end_evt], rel='true'))
        if not items:
            return 'null'
        trig = pf.add('hkbClipTriggerArray')
        trig.param_raw('triggers', '\n'.join(items), numelements=len(items))
        return trig.ref

    root_states_inner = []
    next_id = 1
    default_trans = []      # attackStart_* -> attack states (LOCAL)
    root_wilds = []

    for st, (_names, enter, stop_evt) in SINGLE_PLAY.items():
        if st not in clips['single']:
            continue
        end_evt = 'returnToDefault' if st != 'Death' else None
        clip = _clip(st, clips['single'][st], False,
                     _clip_triggers(st, end_evt))
        root_states_inner.append(_state(
            next_id, f'{st}State', clip.ref,
            exit_events=[stop_evt] if stop_evt else None))
        root_wilds.append((eid[enter], next_id, _F_WILD if st != 'Death'
                           else _F_GLOBAL))
        if st == 'Recoil':
            root_wilds.append((eid['recoilLargeStart'], next_id, _F_WILD))
        next_id += 1

    if 'Swim' in loco:
        swim_clip = _clip('Swim', loco['Swim'], True)
        root_states_inner.append(_state(
            next_id, 'SwimState', swim_clip.ref,
            transitions=[(eid['swimStop'], 0, _F_LOCAL)]))
        root_wilds.append((eid['swimStart'], next_id, _F_WILD))
        next_id += 1

    for kf, evt in zip(clips['attacks'], build_attack_events(clips)):
        st_name = f'Attack_{_clip_state_name(kf)}'
        clip = _clip(st_name, kf, False,
                     _clip_triggers(st_name, 'returnToDefault'))
        mg = pf.add('hkbModifierGenerator')
        mg.param('variableBindingSet', 'null')
        mg.param('userData', 1)
        mg.param('name', f'{st_name}_MG')
        mg.param('modifier', attack_ml.ref)
        mg.param('generator', clip.ref)
        root_states_inner.append(_state(
            next_id, f'{st_name}State', mg.ref,
            exit_events=['attackStop']))
        default_trans.append((eid[evt], next_id, _F_LOCAL))
        next_id += 1

    default_state = _state(0, 'DefaultState', default_sm.ref,
                           transitions=default_trans or None)
    root_states_inner.insert(0, default_state)

    root_wilds.append((eid['returnToDefault'], 0, _F_GLOBAL))
    root_wilds.append((eid['IdleStop'], 0, _F_WILD))
    wild = _trans_array(root_wilds)

    sm = _make_sm(f'{behavior_name}Root', root_states_inner,
                  start_id=0, wildcard_ref=wild.ref)

    # ---- variables / events tables ----
    strings = pf.add('hkbBehaviorGraphStringData')
    strings.param_strings('eventNames', events)
    strings.param_array('attributeNames', [])
    strings.param_strings('variableNames', [n for n, _t, _iv in variables])
    strings.param_array('characterPropertyNames', [])

    values = pf.add('hkbVariableValueSet')
    values.param_structs('wordVariableValues',
                         [[('value', v)] for v in var_values])
    values.param_array('quadVariableValues', [])
    values.param_array('variantVariableValues', [])

    gdata = pf.add('hkbBehaviorGraphData')
    gdata.param_array('attributeDefaults', [])
    gdata.param_raw('variableInfos',
                    '\n'.join(_VARIABLE_INFO_TMPL.format(vtype=t)
                              for _n, t, _iv in variables),
                    numelements=len(variables))
    gdata.param_structs('characterPropertyInfos', [])
    gdata.param_structs('eventInfos', [[('flags', 0)]] * len(events))
    gdata.param('variableInitialValues', values.ref)
    gdata.param('stringData', strings.ref)

    # ---- engine movement hookup (mirrors the vanilla quadruped root) ----
    # The engine samples the graph's animation-driven movement speed through
    # a BSSpeedSamplerModifier bound to iState/Direction/Speed/SpeedSampled.
    # Without it AI pathing has no speed to drive, the actor never receives
    # movement, and it stands in its idle forever (combat can't approach
    # either).  Vanilla wraps the whole locomotion state machine in a
    # hkbModifierGenerator under a single-state root state machine; copy
    # that layout verbatim (userData values included).
    sampler_binds = _binding_set([('state', 'iState'),
                                  ('direction', 'Direction'),
                                  ('goalSpeed', 'Speed'),
                                  ('speedOut', 'SpeedSampled')])

    sampler = pf.add('BSSpeedSamplerModifier')
    sampler.param('variableBindingSet', sampler_binds.ref)
    sampler.param('userData', 2)
    sampler.param('name', 'BSSpeedSamplerModifier')
    sampler.param('enable', True)
    sampler.param('state', -1)
    sampler.param('direction', '0.000000')
    sampler.param('goalSpeed', '0.000000')
    sampler.param('speedOut', '0.000000')

    # ---- combat stance handshake (verbatim vanilla quadruped layout) ----
    # The engine's ActionDraw routes combatStanceStart (IDLE record); combat
    # then WAITS for the graph to reply with a weaponDraw event before ever
    # sending an attackStart_*.  Vanilla sends that reply from a root-level
    # hkbEventDrivenModifier/hkbEvaluateExpressionModifier pair (StartCombat/
    # StopCombat), NOT from state notify events — an actor without this pair
    # chases its target forever and never attacks.
    def _combat_eem(name, stance_val, reply_evt):
        arr = pf.add('hkbExpressionDataArray')
        arr.param_raw('expressionsData', (
            '<hkobject>\n'
            f'\t<hkparam name="expression">iCombatStance = {stance_val}'
            '</hkparam>\n'
            '\t<hkparam name="assignmentVariableIndex">-1</hkparam>\n'
            '\t<hkparam name="assignmentEventIndex">-1</hkparam>\n'
            '\t<hkparam name="eventMode">EVENT_MODE_SEND_ONCE</hkparam>\n'
            '</hkobject>\n'
            '<hkobject>\n'
            f'\t<hkparam name="expression">{reply_evt} if '
            f'(iCombatStance == {stance_val})</hkparam>\n'
            '\t<hkparam name="assignmentVariableIndex">-1</hkparam>\n'
            '\t<hkparam name="assignmentEventIndex">-1</hkparam>\n'
            '\t<hkparam name="eventMode">EVENT_MODE_SEND_ON_FALSE_TO_TRUE'
            '</hkparam>\n'
            '</hkobject>'), numelements=2)
        eem = pf.add('hkbEvaluateExpressionModifier')
        eem.param('variableBindingSet', 'null')
        eem.param('userData', 2)
        eem.param('name', name)
        eem.param('enable', True)
        eem.param('expressions', arr.ref)
        return eem

    def _edm(name, modifier_ref, activate, deactivate, active_by_default):
        edm = pf.add('hkbEventDrivenModifier')
        edm.param('variableBindingSet', 'null')
        edm.param('userData', 1)
        edm.param('name', name)
        edm.param('enable', True)
        edm.param('modifier', modifier_ref)
        edm.param('activateEventId', activate)
        edm.param('deactivateEventId', deactivate)
        edm.param('activeByDefault', active_by_default)
        return edm

    stop_combat = _edm('StopCombat_EDM',
                       _combat_eem('StopCombat_EEM', 0, 'weaponSheathe').ref,
                       eid['combatStanceStop'], eid['combatStanceStart'],
                       True)
    start_combat = _edm('StartCombat_EDM',
                        _combat_eem('StartCombat_EEM', 1, 'weaponDraw').ref,
                        eid['combatStanceStart'], eid['combatStanceStop'],
                        False)

    mod_list = pf.add('hkbModifierList')
    mod_list.param('variableBindingSet', 'null')
    mod_list.param('userData', 1)
    mod_list.param('name', 'RootModifierList')
    mod_list.param('enable', True)
    mod_list.param_array('modifiers', [stop_combat.ref, start_combat.ref,
                                       sampler.ref])

    mod_gen = pf.add('hkbModifierGenerator')
    mod_gen.param('variableBindingSet', 'null')
    mod_gen.param('userData', 1)
    mod_gen.param('name', 'RootModifierGenerator')
    mod_gen.param('modifier', mod_list.ref)
    mod_gen.param('generator', sm.ref)

    def _root_state(state_id, name, generator_ref, enter_evt_name=None,
                    transitions_ref='null'):
        enter_ref = 'null'
        if enter_evt_name:
            arr = pf.add('hkbStateMachineEventPropertyArray')
            arr.param_raw('events', (
                '<hkobject>\n'
                f'\t<hkparam name="id">{eid[enter_evt_name]}</hkparam>\n'
                '\t<hkparam name="payload">null</hkparam>\n'
                '</hkobject>'), numelements=1)
            enter_ref = arr.ref
        st = pf.add('hkbStateMachineStateInfo')
        st.param('variableBindingSet', 'null')
        st.param_array('listeners', [])
        st.param('enterNotifyEvents', enter_ref)
        st.param('exitNotifyEvents', 'null')
        st.param('transitions', transitions_ref)
        st.param('generator', generator_ref)
        st.param('name', name)
        st.param('stateId', state_id)
        st.param('probability', '1.000000')
        st.param('enable', True)
        return st

    root_states = [_root_state(0, 'Root', mod_gen.ref)]
    root_wild_ref = 'null'

    # ---- death / ragdoll wrapper states (vanilla dogbehavior root SM) ----
    # Oblivion creatures ship no death animations — death IS the ragdoll.
    # The engine's kill flow fires ActionDeathWait, whose IDLE tree
    # (tes5_import/creature_idles.py) sends DeathAnimation or Ragdoll; the
    # wrapper states power the ragdoll (skeleton.hkx hkaRagdollInstance) and
    # notify AddRagdollToWorld / RemoveCharacterControllerFromWorld, which
    # the engine itself consumes.  Without them `kill` leaves the actor
    # standing in its idle forever.
    if ragdoll and clips['idle']:
        pb0, pb1, pb2 = ragdoll['pose_bones']

        def _powered_ragdoll(name, max_force, mode):
            bones_arr = pf.add('hkbBoneIndexArray')
            bones_arr.param('variableBindingSet', 'null')
            bones_arr.param_array('boneIndices', [])
            weights = pf.add('hkbBoneWeightArray')
            weights.param('variableBindingSet', 'null')
            weights.param_array('boneWeights', [])
            m = pf.add('hkbPoweredRagdollControlsModifier')
            m.param('variableBindingSet', 'null')
            m.param('userData', 1)
            m.param('name', name)
            m.param('enable', True)
            m.param_raw('controlData', (
                '<hkobject>\n'
                f'\t<hkparam name="maxForce">{max_force:.6f}</hkparam>\n'
                '\t<hkparam name="tau">0.800000</hkparam>\n'
                '\t<hkparam name="damping">1.000000</hkparam>\n'
                '\t<hkparam name="proportionalRecoveryVelocity">2.000000'
                '</hkparam>\n'
                '\t<hkparam name="constantRecoveryVelocity">1.000000'
                '</hkparam>\n'
                '</hkobject>'))
            m.param('bones', bones_arr.ref)
            m.param_raw('worldFromModelModeData', (
                '<hkobject>\n'
                f'\t<hkparam name="poseMatchingBone0">{pb0}</hkparam>\n'
                f'\t<hkparam name="poseMatchingBone1">{pb1}</hkparam>\n'
                f'\t<hkparam name="poseMatchingBone2">{pb2}</hkparam>\n'
                f'\t<hkparam name="mode">{mode}</hkparam>\n'
                '</hkobject>'))
            m.param('boneWeights', weights.ref)
            return m

        def _mod_state_gen(name, modifiers, clip_name):
            # objects must be defined before their referencers (hkxcmd
            # parser rejects forward references)
            pose_clip = _clip(clip_name, clips['idle'], True)
            lst = pf.add('hkbModifierList')
            lst.param('variableBindingSet', 'null')
            lst.param('userData', 0)
            lst.param('name', f'{name}_ML')
            lst.param('enable', True)
            lst.param_array('modifiers', [m.ref for m in modifiers])
            gen = pf.add('hkbModifierGenerator')
            gen.param('variableBindingSet', 'null')
            gen.param('userData', 1)
            gen.param('name', f'{name}_MG')
            gen.param('modifier', lst.ref)
            gen.param('generator', pose_clip.ref)
            return gen

        # falling-over stage: powered ragdoll drives toward the animated
        # pose; the contact listener converts floor contact into a Ragdoll
        # event (vanilla CollisionListener), completing the collapse
        contact_bones = pf.add('hkbBoneIndexArray')
        contact_bones.param('variableBindingSet', 'null')
        contact_bones.param_array('boneIndices',
                                  list(range(ragdoll['parts'])))
        listener = pf.add('BSRagdollContactListenerModifier')
        listener.param('variableBindingSet', 'null')
        listener.param('userData', 2)
        listener.param('name', 'CollisionListener')
        listener.param('enable', True)
        listener.param_raw('contactEvent', (
            '<hkobject>\n'
            f'\t<hkparam name="id">{eid["Ragdoll"]}</hkparam>\n'
            '\t<hkparam name="payload">null</hkparam>\n'
            '</hkobject>'))
        listener.param('bones', contact_bones.ref)

        anim2rag = _mod_state_gen(
            'AnimateToRagdoll',
            [_powered_ragdoll('PoweredRagdollMatching', 200.0,
                              'WORLD_FROM_MODEL_MODE_COMPUTE'), listener],
            'AnimateToRagdollPose')
        full_rag = _mod_state_gen(
            'FullyRagdoll',
            [_powered_ragdoll('PoweredRagdollNoMatching', 0.0,
                              'WORLD_FROM_MODEL_MODE_RAGDOLL')],
            'FullyRagdollPose')

        rag_fx = pf.add('hkbBlendingTransitionEffect')
        rag_fx.param('variableBindingSet', 'null')
        rag_fx.param('userData', 0)
        rag_fx.param('name', 'RagdollBlend')
        rag_fx.param('selfTransitionMode',
                     'SELF_TRANSITION_MODE_CONTINUE_IF_CYCLIC')
        rag_fx.param('eventMode', 'EVENT_MODE_DEFAULT')
        rag_fx.param('duration', '0.200000')
        rag_fx.param('toGeneratorStartTimeFraction', '0.000000')
        rag_fx.param('flags', 'FLAG_IGNORE_FROM_WORLD_FROM_MODEL')
        rag_fx.param('endMode', 'END_MODE_NONE')
        rag_fx.param('blendCurve', 'BLEND_CURVE_SMOOTH')

        a2r_trans = pf.add('hkbStateMachineTransitionInfoArray')
        a2r_trans.param_raw(
            'transitions',
            _TRANSITION_TMPL.format(effect=rag_fx.ref,
                                    event_id=eid['Ragdoll'], to_state=2,
                                    flags=_F_WILD),
            numelements=1)

        root_states.append(_root_state(1, 'AnimateToRagdoll', anim2rag.ref,
                                       'AddRagdollToWorld', a2r_trans.ref))
        root_states.append(_root_state(
            2, 'Fully Ragdoll', full_rag.ref,
            'RemoveCharacterControllerFromWorld'))

        root_wild = pf.add('hkbStateMachineTransitionInfoArray')
        root_wild.param_raw(
            'transitions',
            '\n'.join(_TRANSITION_TMPL.format(effect=e, event_id=ev,
                                              to_state=s, flags=_F_WILD)
                      for ev, s, e in (
                          (eid['DeathAnimation'], 1, rag_fx.ref),
                          (eid['Ragdoll'], 2, rag_fx.ref),
                          (eid['RagdollInstant'], 2, 'null'))),
            numelements=3)
        root_wild_ref = root_wild.ref

    root_sm = pf.add('hkbStateMachine')
    root_sm.param('variableBindingSet', 'null')
    root_sm.param('userData', 0)
    root_sm.param('name', f'{behavior_name}RootBehavior')
    root_sm.param_raw('eventToSendWhenStateOrTransitionChanges', (
        '<hkobject>\n\t<hkparam name="id">-1</hkparam>\n'
        '\t<hkparam name="payload">null</hkparam>\n</hkobject>'))
    root_sm.param('startStateChooser', 'null')
    root_sm.param('startStateId', 0)
    root_sm.param('returnToPreviousStateEventId', -1)
    root_sm.param('randomTransitionEventId', -1)
    root_sm.param('transitionToNextHigherStateEventId', -1)
    root_sm.param('transitionToNextLowerStateEventId', -1)
    root_sm.param('syncVariableIndex', -1)
    root_sm.param('wrapAroundStateId', False)
    root_sm.param('maxSimultaneousTransitions', 32)
    root_sm.param('startStateMode', 'START_STATE_MODE_DEFAULT')
    root_sm.param('selfTransitionMode', 'SELF_TRANSITION_MODE_NO_TRANSITION')
    root_sm.param_array('states', [s.ref for s in root_states])
    root_sm.param('wildcardTransitions', root_wild_ref)

    graph = pf.add('hkbBehaviorGraph')
    graph.param('variableBindingSet', 'null')
    graph.param('userData', 0)
    graph.param('name', f'{behavior_name}.hkb')
    graph.param('variableMode', 'VARIABLE_MODE_DISCARD_WHEN_INACTIVE')
    graph.param('rootGenerator', root_sm.ref)
    graph.param('data', gdata.ref)

    top = pf.add('hkRootLevelContainer')
    top.param_structs('namedVariants', [
        [('name', 'hkbBehaviorGraph'), ('className', 'hkbBehaviorGraph'),
         ('variant', graph.ref)]])
    return pf.render(top)


