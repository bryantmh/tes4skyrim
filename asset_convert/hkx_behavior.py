"""Generate a complete Skyrim LE creature behavior project from Oblivion data.

Emits per creature (all via packfile XML → hkxcmd, layouts copied from the
vanilla deer project dumps):

  actors/tes4/<name>/<name>project.hkx          (hkbProjectData boilerplate)
  actors/tes4/<name>/characters/<name>character.hkx  (hkbCharacterData: rig +
                                                 animation list + controller)
  actors/tes4/<name>/behaviors/<name>behavior.hkx    (GENERATED state machine)
  actors/tes4/<name>/character assets/skeleton.hkx   (from hkx_skeleton)
  actors/tes4/<name>/animations/*.hkx                (from hkx_anim)
  actors/tes4/<name>/motion_data.json                (root motion per clip —
                                                 consumed by the future
                                                 animationdata emitter)

v1 graph: one root hkbStateMachine — Idle (start, looping) + locomotion states
(forward/backward/turns/swim, looping) + one single-play state per attack /
recoil / stagger clip, entered by the standard engine events (wildcard
transitions) and exited by clip-end triggers (attackStop etc. → Idle). This is
the Oblivion animation-selection convention expressed as a graph; ragdoll
death, foot IK, look-at, and speed-blended gaits are later refinements
(vanilla wrapper-graph features).

Event names follow the vanilla creature vocabulary (quadrupedbehavior.hkx),
so RACE ATKD/ATKE records generated by tes5_import must use the same
`attackStart_TES4_<clip>` strings (build_attack_events()).
"""

