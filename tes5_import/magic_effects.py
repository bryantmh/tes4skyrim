"""Companion MGEF generation — aimed variants of vanilla magic effects.

Skyrim ships no fire-and-forget/aimed MGEF for plain value modifiers (Restore
Health on target, Damage Attribute, Weakness to X, Cure ...), so the mapping
tables resolve those TES4 effect codes to Alch*/self-delivery effects.  That is
fine for potions and touch spells, but an AIMED magic item whose effects all
lack a projectile cannot fire: the engine takes the projectile to launch from
the item's effects, and with none found the cast does nothing (CK: "Magic Item
... is AIMED but has no Magic Effects with Projectiles assigned", 369 items).

Fix: when an aimed ENCH/SPEL/SCRL ends up with no projectile-bearing effect,
synthesize ONE companion MGEF here — a clone of the vanilla effect the primary
effect resolved to, patched to Fire and Forget + Aimed + a projectile — and
swap it in for that first effect.  The remaining effects stay as mapped; they
apply on impact alongside it.

The 152-byte vanilla DATA blobs come from tes5_import/vanilla_mgef_data.py,
generated by tools/gen_vanilla_mgef_table.py from the Skyrim.esm dump (end
users don't have references/).
"""
import struct
import threading

from .vanilla_mgef_data import VANILLA_MGEF_DATA
from .writer import pack_record, pack_string_subrecord, pack_subrecord

# MGEF DATA offsets (TES5, 152 bytes — see xEdit wbDefinitionsTES5 Magic Effect
# Data and the layout constants in tools/gen_vanilla_mgef_table.py's source).
_OFF_FLAGS = 0x00
_OFF_COUNTER_COUNT = 0x14      # counter-effect count — must match ESCE entries
_OFF_PROJECTILE = 0x48
_OFF_CAST_TYPE = 0x50          # 0 constant, 1 fire and forget, 2 concentration
_OFF_DELIVERY = 0x54           # 0 self, 1 touch, 2 aimed, 3 target actor
_HOSTILE = 0x00000001

# Projectiles for the synthesized effects, picked from vanilla aimed MGEFs so
# the bolt has real art/flight data.  Hostile effects fly the ghostly bolt of
# dunTrevasSpectralArrowDamageFFAimed; beneficial ones the golden sunfire of
# TurnUndeadFFAimed25.
_PROJ_HOSTILE = 0x000AB23B     # dunTrevasSpectralArrowProjectile
_PROJ_BENEFICIAL = 0x0004BE35  # TurnProjectile01

# TES4 effect code -> display name ("DGHE" -> "Damage Health"), built from the
# export's own MGEF records so the synthesized effect is named what Oblivion
# called it on the item card.
_tes4_names: dict = {}

# (vanilla fid, tes4 code) -> generated MGEF fid.  Keyed per code so a Burden
# staff shows "Burden", not the name of whichever code shares its vanilla
# mapping.  Converters run in a thread pool, hence the lock.
_cache: dict = {}
_lock = threading.Lock()


def set_tes4_effect_names(mgef_records: list) -> None:
    """Register TES4 MGEF display names ({4-char code: FULL})."""
    from .text_reader import get_str
    _tes4_names.clear()
    _cache.clear()
    for rec in mgef_records:
        code = get_str(rec, 'EditorID')
        full = get_str(rec, 'FULL')
        if code and full:
            _tes4_names[code] = full


def has_projectile(mgef_fid: int) -> bool:
    """True if the vanilla MGEF launches a projectile (or is one of ours)."""
    entry = VANILLA_MGEF_DATA.get(mgef_fid)
    if entry is None:
        # Not a vanilla effect — the only other fids flowing through effect
        # lists are variants this module generated, which all carry one.
        return mgef_fid in _cache.values()
    data = bytes.fromhex(entry[1])
    return struct.unpack_from('<I', data, _OFF_PROJECTILE)[0] != 0


def aimed_variant(mgef_fid: int, tes4_code: str, writer) -> int:
    """FormID of an aimed clone of ``mgef_fid``, generating it on first use.

    Returns 0 when the vanilla effect is unknown (nothing to clone).
    """
    entry = VANILLA_MGEF_DATA.get(mgef_fid)
    if entry is None or writer is None:
        return 0
    with _lock:
        key = (mgef_fid, tes4_code)
        cached = _cache.get(key)
        if cached:
            return cached

        edid, data_hex = entry
        data = bytearray(bytes.fromhex(data_hex))
        hostile = struct.unpack_from('<I', data, _OFF_FLAGS)[0] & _HOSTILE
        proj = _PROJ_HOSTILE if hostile else _PROJ_BENEFICIAL
        struct.pack_into('<I', data, _OFF_PROJECTILE, proj)
        struct.pack_into('<I', data, _OFF_CAST_TYPE, 1)
        struct.pack_into('<I', data, _OFF_DELIVERY, 2)
        # The clone carries no ESCE subrecords, so the counter-effect count
        # must be zeroed or the CK reads garbage counter slots.
        struct.pack_into('<I', data, _OFF_COUNTER_COUNT, 0)

        fid = writer.alloc_formid()
        subs = pack_string_subrecord('EDID', f'TES4{tes4_code}{edid}Aimed')
        full = _tes4_names.get(tes4_code)
        if full:
            subs += pack_string_subrecord('FULL', full)
        subs += pack_subrecord('DATA', bytes(data))
        writer.add_record('MGEF', pack_record('MGEF', fid, 0, subs))
        _cache[key] = fid
        return fid
