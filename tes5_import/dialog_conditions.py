    # drop them rather than emit a condition that can never pass.
    53,    # GetScriptVariable  -> 630 GetVMScriptVariable (strings path only)
    79,    # GetQuestVariable   -> 629 GetVMQuestVariable  (strings path only)
    76,    # GetDisposition — removed (disposition system gone)
    81,    # reused: TES5 IsRotating
    104,   # IsYielding — removed
    109,   # reused: TES5 IsWeaponSkillType (semantics changed)
    160,   # GetFurnitureMarkerID — removed
    171,   # IsPlayerInJail — removed
    180,   # reused: TES5 HasSameEditorLocAsRef
    197,   # reused: TES5 GetPCEnemyofFaction
    201,   # GetPCFactionSubmitAuthority — removed
    224,   # reused: TES5 GetVATSMode  (was GetIsPlayerBirthsign)
    227,   # reused: TES5 GetCannibal
    249,   # reused: TES5 IsInDialogueWithPlayer (was GetPCFame) — critical: see skill
    251,   # GetPCInfamy — removed
    258,   # reused: TES5 HasAssociationType
    259,   # reused: TES5 HasFamilyRelationship
    264,   # reused: TES5 HasSpell (was GetBarterGold)
    274,   # reused: TES5 IsSmallBump
    305,   # reused: TES5 GetPlayerAction
    313,   # reused: TES5 GetPairedAnimation
    323,   # reused: TES5 GetCombatState
    329,   # reused: TES5 IsFleeing
    362,   # reused: TES5 HasLinkedRef
    365,   # reused: TES5 IsChild
    # OBSE supplemental indices with no TES5 equivalent
    1107,  # IsAmmo
    1124,  # IsClassSkill
    1884,  # GetPCTrainingSessionsUsed
    # TES4 package-query funcs (2213-2233) — packages are restructured; drop
    2213, 2214, 2215, 2216, 2217, 2218, 2219, 2220, 2221, 2222, 2223, 2224,
    2225, 2226, 2227, 2228, 2229, 2230, 2231, 2232, 2233,
    2571,  # GetBaseAV3 — removed
    2573,  # IsNaked — removed
    2578,  # IsDiseased — removed
